public class Calculator {
    public  int sum(int first, int second) {
        int result = first + second;
        return result;
    }

    public  float substract(float first, float second){
        float result = first - second;
        return result;
    }

    public  float divide(float first, float second){
        float result = first / second;
        return result;
    }

}
